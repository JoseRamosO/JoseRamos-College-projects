    public void updateTableData() {
        List<Document> Senderos = MongoConnect.getCollectionDocuments("Senderos");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        ((DefaultTableModel) jTable1.getModel()).setNumRows(0);
        for (Document Sendero : Senderos) {
            model.addRow(new Object[]{Sendero.get("_id").toString(), Sendero.getString("nombre"), Sendero.getString("experiencia"), Sendero.getString("calificacion"), "Column 5"});
        }
    }

    public void UpdateSenderoInfo(String IdSendero) {
        Document Sendero = MongoConnect.getCollectionDocumentID("Senderos", IdSendero);
        jTextNombre.setText(Sendero.getString("nombre"));
        jTextUbi.setText(Sendero.getString("ubicacion"));
        jTextExpe.setText(Sendero.getString("experiencia"));
        jTextCali.setText(Sendero.getString("calificacion"));
        jTextDes.setText(Sendero.getString("descripcion"));
        try {
            String ImageUserAdded = Sendero.getString("fotografia");
            BufferedImage img = ImageIO.read(new URL(ImageUserAdded));
            lblmage.setIcon(new javax.swing.ImageIcon(img));
            lblmage.setText("");
        } catch (IOException ex) {
            System.out.println("ERROR CON IMAGEN");
        }
    }

